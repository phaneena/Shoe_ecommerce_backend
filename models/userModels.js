const mongoose = require("mongoose");

const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  isAdmin: {
    type: Boolean,
    required: true,
    default: false,
  },
  isBlock: {
    type: Boolean,
    required: true,
    default: false,
  },
  
},
{
  timestamps:true
});
const User = mongoose.model("user", userSchema);

module.exports = User;
